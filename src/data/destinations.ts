export function searchDestinations(query: string, limit = 8): Destination[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return ALL_DESTINATIONS.filter(d => {
    const text = `${d.label} ${d.keywords ?? ''}`.toLowerCase();
    return text.includes(q);
  }).slice(0, limit);
}
